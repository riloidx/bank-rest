package org.matvey.bankrest.entity;

/**
 * Перечисление статусов банковской карты.
 * Определяет возможные состояния карты в системе.
 */
public enum CardStatus {
    /** Карта активна и может использоваться */
    ACTIVE,
    /** Карта заблокирована пользователем или системой */
    BLOCKED,
    /** Срок действия карты истек */
    EXPIRED
}
